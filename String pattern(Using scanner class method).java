import java.util.*;
class ca
{
    public static void main()
    {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter your name or anything to develope a pattern.");
        String n=sc.nextLine();
        int l=n.length();
        char ch;
        for(int i=l;i>=0;i--)
        {
            for (int j=0;j<l-i;j++)
            {
                ch=n.charAt(j);
                System.out.print(ch);
            }
            System.out.println();
        }
    }
}
